# Jeg lager en variabel for det første tallet slik at jeg lett kan endre verdien senere
tall1 = 3  

# Jeg lager en variabel for det andre tallet, samme grunn: lett å endre uten å endre hele koden
tall2 = 5  

# Jeg ganger tallene sammen og lagrer resultatet i en egen variabel
produkt = tall1 * tall2  

# Jeg lager en variabel for en tekst, så det blir lettere å printe en forståelig melding
melding = f"Resultatet av {tall1} ganger {tall2} er {produkt}"

# Til slutt skriver jeg ut meldingen
print(melding)